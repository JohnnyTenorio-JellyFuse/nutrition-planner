/* ── CLAUDE.md Editor ────────────────────────────────────────────────────── */

function onEditorInput() {
  clearTimeout(saveTimer);
  const status = document.getElementById('save-status');
  status.textContent = 'Saving...';
  status.classList.remove('saved');

  saveTimer = setTimeout(() => {
    localStorage.setItem('claude_md', document.getElementById('claude-editor').value);
    status.textContent = '✓ Saved';
    status.classList.add('saved');
  }, 700);
}

function downloadMd() {
  const content = document.getElementById('claude-editor').value;
  const blob = new Blob([content], { type: 'text/markdown;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = 'CLAUDE.md';
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

function copyMd() {
  navigator.clipboard.writeText(document.getElementById('claude-editor').value).then(() => {
    const btn = event.currentTarget;
    const original = btn.textContent;
    btn.textContent = '✓ Copied';
    setTimeout(() => { btn.textContent = original; }, 2000);
  });
}

function resetMd() {
  if (!confirm('Reset CLAUDE.md to the original template? Your edits will be lost.')) return;
  document.getElementById('claude-editor').value = DEFAULT_MD;
  localStorage.setItem('claude_md', DEFAULT_MD);
  const status = document.getElementById('save-status');
  status.textContent = '✓ Reset';
  status.classList.add('saved');
}

function applyQuickUpdate() {
  const weight = document.getElementById('qf-weight').value;
  const notes = document.getElementById('qf-notes').value.trim();
  let content = document.getElementById('claude-editor').value;

  // Update starting weight line in the stats table
  if (weight) {
    content = content.replace(
      /\| Starting weight \| .+ \|/,
      `| Starting weight | ${weight} lbs |`
    );
    settings.currentWeight = parseFloat(weight);
    document.getElementById('s-weight').value = weight;
    localStorage.setItem('nutrition_settings', JSON.stringify(settings));
  }

  // Append a timestamped note to the Program Notes section
  if (notes) {
    const today = new Date().toISOString().split('T')[0];
    const entry = `\n- [${today}] ${notes}`;
    const marker = '_(Update this section as you progress — current weight, what\'s working, swaps made, etc.)_';
    if (content.includes(marker)) {
      content = content.replace(marker, marker + entry);
    } else {
      content += `\n\n## Latest Notes\n\n- [${today}] ${notes}`;
    }
  }

  document.getElementById('claude-editor').value = content;
  localStorage.setItem('claude_md', content);

  const status = document.getElementById('save-status');
  status.textContent = '✓ Updated';
  status.classList.add('saved');

  // Clear quick fields and switch to editor tab
  document.getElementById('qf-weight').value = '';
  document.getElementById('qf-day').value = '';
  document.getElementById('qf-notes').value = '';

  const editorTab = document.querySelectorAll('.tab')[3];
  switchTab('editor', editorTab);
}

