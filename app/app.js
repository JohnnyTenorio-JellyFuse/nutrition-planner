/* ── State ───────────────────────────────────────────────────────────────── */

let settings = { startDate: '', currentWeight: 190, dayOverride: '', water: 0 };
let weightLog = [];
let saveTimer = null;

/* ── Init ────────────────────────────────────────────────────────────────── */

function init() {
  // Load CLAUDE.md from localStorage
  const savedMd = localStorage.getItem('claude_md') || DEFAULT_MD;
  document.getElementById('claude-editor').value = savedMd;

  // Load settings
  const savedSettings = localStorage.getItem('nutrition_settings');
  if (savedSettings) settings = JSON.parse(savedSettings);

  // Load weight log
  const savedLog = localStorage.getItem('weight_log');
  if (savedLog) weightLog = JSON.parse(savedLog);

  // Apply saved settings to inputs
  if (settings.startDate)    document.getElementById('s-start').value = settings.startDate;
  if (settings.currentWeight) document.getElementById('s-weight').value = settings.currentWeight;
  if (settings.dayOverride)  document.getElementById('s-day-override').value = settings.dayOverride;
  if (settings.water)        document.getElementById('s-water').value = settings.water;

  updateDashboard();
  renderWeightLog();
  renderRecipes();
}

/* ── Dashboard ───────────────────────────────────────────────────────────── */

function getProgramDay() {
  if (settings.dayOverride) return parseInt(settings.dayOverride, 10);
  if (!settings.startDate) return 0;
  const start = new Date(settings.startDate + 'T00:00:00');
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const diff = Math.floor((today - start) / (1000 * 60 * 60 * 24)) + 1;
  return Math.max(1, Math.min(diff, 60));
}

function updateDashboard() {
  const d = getProgramDay();
  const idx = d > 0 ? (d - 1) % 5 : 0;
  const plan = PLAN_DAYS[idx];
  const totalP = plan.meals.reduce((sum, m) => sum + m.p, 0);
  const totalCal = plan.meals.reduce((sum, m) => sum + m.cal, 0);

  // Program day number
  document.getElementById('prog-day-num').textContent = d > 0 ? d : '—';
  document.getElementById('prog-day-sub').textContent = d > 0
    ? `${60 - d} days remaining · ${plan.name}`
    : 'Set your start date in Program Settings';

  // Progress bar
  const pct = Math.min(Math.max((d / 60) * 100, 0), 100);
  document.getElementById('prog-bar').style.width = pct + '%';
  document.getElementById('prog-l').textContent = d > 0 ? `Day ${d}` : 'Day 0';

  // Today badge
  const badge = document.getElementById('today-badge');
  badge.innerHTML = d > 0
    ? `<div class="today-badge">📅 Today: <strong style="margin:0 4px">${plan.name}</strong> · ${totalP}g P · ${totalCal} cal</div>`
    : '';

  // Today's meals
  document.getElementById('today-meals').innerHTML = plan.meals.map(m => `
    <div class="meal-row">
      <div class="meal-time">${m.time}</div>
      <div class="meal-name">${m.name}</div>
      <div>
        <div class="meal-p">${m.p}g</div>
        <div class="meal-cal">${m.cal} cal</div>
      </div>
    </div>
  `).join('');
}

/* ── Settings ────────────────────────────────────────────────────────────── */

function saveSettings() {
  settings.startDate     = document.getElementById('s-start').value;
  settings.currentWeight = parseFloat(document.getElementById('s-weight').value) || 190;
  settings.dayOverride   = document.getElementById('s-day-override').value;
  settings.water         = parseFloat(document.getElementById('s-water').value) || 0;
  localStorage.setItem('nutrition_settings', JSON.stringify(settings));
  updateDashboard();
}

/* ── Weight Log ──────────────────────────────────────────────────────────── */

function addWeightLog() {
  const date = document.getElementById('log-date').value;
  const weight = parseFloat(document.getElementById('log-w').value);
  if (!date || !weight) return;

  // Replace existing entry for same date
  weightLog = weightLog.filter(e => e.date !== date);
  weightLog.push({ date, weight });
  weightLog.sort((a, b) => b.date.localeCompare(a.date));

  localStorage.setItem('weight_log', JSON.stringify(weightLog));
  document.getElementById('log-date').value = '';
  document.getElementById('log-w').value = '';
  renderWeightLog();
}

function renderWeightLog() {
  const el = document.getElementById('wlog-list');
  if (!weightLog.length) {
    el.className = 'wlog-empty';
    el.textContent = 'No entries yet.';
    return;
  }
  el.className = '';
  el.innerHTML = weightLog.slice(0, 20).map(e => {
    const diff = (e.weight - 190).toFixed(1);
    const color = diff <= 0 ? 'var(--accent)' : 'var(--red)';
    const sign = diff <= 0 ? '' : '+';
    return `<div class="wlog-row">
      <span style="color:var(--muted)">${e.date}</span>
      <span>
        <strong style="color:var(--text)">${e.weight} lbs</strong>
        &nbsp;<span style="color:${color}">${sign}${diff}</span>
      </span>
    </div>`;
  }).join('');
}

